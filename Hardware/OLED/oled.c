// oled.c - SSD1306 OLED 128x64 I2C 显示驱动
// 
// GDDRAM组织: 128列 × 64行 = 8页(Page0~7) × 128段(SEG0~127)
// 每页8像素高, Page0=屏幕顶部(Y=0~7), Page7=底部(Y=56~63)
// 每列1字节, LSB(bit0)对应页内最顶部像素, MSB(bit7)对应最底部
// 
// I2C通信: 控制字节0x00=命令, 0x40=GDDRAM数据
// 从机地址: 7位0x3C, 写=0x78
// 
// 页寻址模式: 指定page+起始col后, 每写1字节col自动+1, 方便连续写字符串。
// 
// 字库: 6x8像素ASCII (95字符, 0x20~0x7E), 每字符6列(5+1间距)
// 每行最多128/6≈21字符

#include "oled.h"
#include "i2c.h"
#include "delay.h"

#define OLED_ADDR_W  0x78  // I2C写地址: 7位0x3C左移1位

// 发送命令 (控制字节0x00)
static void OLED_WriteCmd(uint8_t cmd)
{
	I2C_Start();
	I2C_WriteByte(OLED_ADDR_W);
	I2C_WriteByte(0x00);  // 0x00 = 下一个字节是命令
	I2C_WriteByte(cmd);
	I2C_Stop();
}

// 发送数据 (控制字节0x40)
static void OLED_WriteData(uint8_t data)
{
	I2C_Start();
	I2C_WriteByte(OLED_ADDR_W);
	I2C_WriteByte(0x40);  // 0x40 = 后续是GDDRAM数据
	I2C_WriteByte(data);
	I2C_Stop();
}

