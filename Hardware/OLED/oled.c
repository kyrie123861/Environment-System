// SSD1306 初始化命令序列 (按datasheet推荐顺序):
// 0xAE       关显示 (初始化期间避免花屏)
// 0x20,0x02  页寻址模式
// 0xB0       起始页=Page0
// 0xC8       COM扫描从COM63到COM0 (上下不翻转)
// 0x00,0x10  列地址=0 (低4位, 高4位分开发送)
// 0x40       显示起始行=0
// 0x81,0xFF  对比度=255 (最亮)
// 0xA1       段重映射 SEG0=列127 (左右不翻转)
// 0xA6       正常显示 (1=亮,0=暗,非反白)
// 0xA8,0x3F  多路复用比=64 (1/64 duty)
// 0xA4       输出RAM内容 (非全屏点亮)
// 0xD3,0x00  垂直偏移=0
// 0xD5,0xF0  时钟分频比=1, 振荡频率=最大
// 0xD9,0x22  预充电周期 Phase1=2, Phase2=2
// 0xDA,0x12  COM硬件配置: 顺序+左右反相
// 0xDB,0x20  VCOMH电压 ~0.77*VCC
// 0x8D,0x14  电荷泵使能 (3.3V屏必须, 否则不亮!)
// 0xAF       开显示
void OLED_Init(void)
{
	uint8_t i;
	uint8_t init_cmds[] = {
		0xAE,  // 关显示
		0x20, 0x02,  // 页寻址
		0xB0,  // 起始页
		0xC8,  // COM扫描方向
		0x00,  // 列低4位
		0x10,  // 列高4位
		0x40,  // 起始行
		0x81, 0xFF,  // 对比度最大
		0xA1,  // 段重映射
		0xA6,  // 正常显示
		0xA8, 0x3F,  // 1/64 duty
		0xA4,  // 正常输出
		0xD3, 0x00,  // 垂直偏移0
		0xD5, 0xF0,  // 时钟
		0xD9, 0x22,  // 预充电
		0xDA, 0x12,  // COM配置
		0xDB, 0x20,  // VCOMH
		0x8D, 0x14,  // 电荷泵使能
		0xAF  // 开显示
	};

	for (i = 0; i < sizeof(init_cmds); i++)
		OLED_WriteCmd(init_cmds[i]);

	OLED_Clear();  // 初始化后清屏
}

// 设置GDDRAM写入位置
// 页地址: 0xB0 | (page & 0x07), page范围0~7
// 列地址分两次发: 低4位(0x00|col[3:0]), 高4位(0x10|col[7:4])
void OLED_SetPos(uint8_t page, uint8_t col)
{
	if (page > 7)  page = 7;
	if (col  > 127) col  = 127;

	OLED_WriteCmd(0xB0 | (page & 0x07));
	OLED_WriteCmd(0x00 | (col & 0x0F));
	OLED_WriteCmd(0x10 | ((col >> 4) & 0x0F));
}

// 清除页面从col_start到行末: 写空格字符 (0x00) 填充
// 用于擦除旧数值残留像素, 每次显示前调用
void OLED_ClearLine(uint8_t page, uint8_t col_start)
{
	uint8_t col;
	OLED_SetPos(page, col_start);
	for (col = col_start; col < 128; col++)
		OLED_WriteData(0x00);
}

// 清屏: 遍历8页×128列, 全部写0x00 (像素熄灭)
void OLED_Clear(void)
{
	uint8_t page;
	uint16_t col;

	for (page = 0; page < 8; page++)
	{
		OLED_SetPos(page, 0);
		for (col = 0; col < 128; col++)
			OLED_WriteData(0x00);
	}
}

// 在指定位置显示一个6x8 ASCII字符
// 超出可打印范围(0x20~0x7E)的显示空格
// 字库索引 = ch - 0x20
void OLED_ShowChar(uint8_t page, uint8_t col, char ch)
{
	uint8_t idx, i;

	if (ch < 0x20 || ch > 0x7E)
		ch = ' ';  // 不可显示字符 -> 空格

	idx = ch - 0x20;  // 字库索引 (0~94)

	OLED_SetPos(page, col);

	for (i = 0; i < 6; i++)  // 写6列像素数据
		OLED_WriteData(font6x8[idx][i]);
}

// 显示字符串: 每个字符6像素宽, col>122(128-6)时截断
void OLED_ShowString(uint8_t page, uint8_t col, const char *str)
{
	while (*str && col <= 122)
	{
		OLED_ShowChar(page, col, *str);
		col += 6;  // 下个字符位置
		str++;
	}
}

// 显示整数(支持负数): 转为字符串后显示
// 逐位取模存入buf, 反转后输出 (取模结果是逆序的)
void OLED_ShowInt(uint8_t page, uint8_t col, int num)
{
	char buf[8];
	uint8_t i = 0, j;
	uint8_t neg = 0;

	if (num < 0) { neg = 1; num = -num; }

	if (num == 0)
		buf[i++] = '0';
	else
		while (num > 0 && i < 7)
		{
			buf[i++] = '0' + (num % 10);  // 取个位
			num /= 10;  // 去个位
		}

	if (neg) buf[i++] = '-';
	buf[i] = '\0';

// 反转 (取模结果是低位在前)
	for (j = 0; j < i / 2; j++)
	{
		char t = buf[j];
		buf[j] = buf[i - 1 - j];
		buf[i - 1 - j] = t;
	}

	OLED_ShowString(page, col, buf);
}

// 显示浮点数(保留1位小数): 分离整数和小数部分, 插入'.'
// 四舍五入: dPart = (num - iPart)*10 + 0.5
void OLED_ShowFloat(uint8_t page, uint8_t col, float num)
{
	char buf[10];
	uint8_t pos = 0;
	int iPart, dPart;

	if (num < 0) { buf[pos++] = '-'; num = -num; }

	iPart = (int)num;
	dPart = (int)((num - iPart) * 10.0f + 0.5f);  // 四舍五入

// 处理进位: 如25.96 -> dPart=10 -> dPart=0, iPart++
	if (dPart >= 10) { dPart = 0; iPart++; }

// 整数部分转字符串
	if (iPart == 0)
		buf[pos++] = '0';
	else
	{
		uint8_t t = pos;
		while (iPart > 0 && t < 9)
		{
			buf[t++] = '0' + (iPart % 10);
			iPart /= 10;
		}
// 反转整数部分
		{
			uint8_t j;
			uint8_t n = t - pos;
			for (j = 0; j < n / 2; j++)
			{
				char c = buf[pos + j];
				buf[pos + j] = buf[t - 1 - j];
				buf[t - 1 - j] = c;
			}
		}
		pos = t;
	}

	buf[pos++] = '.';
	buf[pos++] = '0' + dPart;
	buf[pos] = '\0';

	OLED_ShowString(page, col, buf);
}
